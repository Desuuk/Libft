#include "libft.h"

char	*ft_strncpy(char *dest, char *src, int size)
{
	int i;

	i = 0;
	if(!(dest) || !(src) || !(size))
		return 0;
	while (src[i] && i < size)
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}
